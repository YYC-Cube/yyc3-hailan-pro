def generate_main_document(category, doc_id, doc_name, doc_desc, tags):
    """生成主文档内容"""
    safe_doc_name = doc_name.replace('/', '-').replace('\\', '-')
    file_name = f"{doc_id}-{safe_doc_name.replace(' ', '-')}.md"
    title = f"{doc_id} {doc_name}"

    content = MAIN_MD_TEMPLATE.format(
        FILE_NAME=file_name,
        DESCRIPTION=doc_desc,
        VERSION=VERSION,
        CREATE_DATE=CREATE_DATE,
        STATUS=STATUS,
        TAGS=tags,
        TITLE=title,
        DOC_CATEGORY=category,
        DOC_NAME=doc_name
    )

    return file_name, content

def generate_readme_document(category, category_desc):
    """生成README索引文档"""
    file_name = "README.md"
    title = f"{category} 文档索引"

    content = README_MD_TEMPLATE.format(
        DESCRIPTION=category_desc,
        VERSION=VERSION,
        CREATE_DATE=CREATE_DATE,
        STATUS=STATUS,
        TAGS=f"[{category}],[文档索引]",
        DOC_CATEGORY=category
    )

    return file_name, content

def generate_all_documents():
    """生成所有文档"""
    print("📝 开始生成文档...\n")

    total_docs = 0
    category_docs = {}

    for category, docs in PROJECT_STRUCT.items():
        category_path = os.path.join(DOC_ROOT, category)
        category_docs[category] = 0

        print(f"📂 处理分类: {category}")

        for doc in docs:
            doc_id, doc_name, doc_desc = doc[0], doc[1], doc[2]
            tags = f"[{category}],[{doc_desc.split('[')[1].rstrip(']') if '[' in doc_desc else ''}]"
            file_name, content = generate_main_document(
                category, doc_id, doc_name, doc_desc, tags
            )

            file_path = os.path.join(category_path, file_name)
            with open(file_path, 'w', encoding=ENCODING) as f:
                f.write(content)

            category_docs[category] += 1
            total_docs += 1
            print(f"  ✅ 生成文档: {file_name}")

        print(f"  📊 {category} 共生成 {category_docs[category]} 个文档\n")

    print(f"🎉 文档生成完成！总计生成 {total_docs} 个文档\n")

