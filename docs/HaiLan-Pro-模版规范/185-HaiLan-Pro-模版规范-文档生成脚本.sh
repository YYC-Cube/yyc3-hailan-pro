# ===================== 主函数：生成文档 =====================
def main():
    print("=== HaiLan Pro 文档架构一键生成脚本 ===")
    print(f"文档根目录: {DOC_ROOT}")
    print(f"创建日期: {CREATE_DATE}")
    print(f"版本: {VERSION}")
    print()

    for category, docs in PROJECT_STRUCT.items():
        category_path = os.path.join(DOC_ROOT, category)
        os.makedirs(category_path, exist_ok=True)
        
        for doc_num, doc_name, doc_desc, tags in docs:
            file_name = f"{doc_num}-{doc_name}.md"
            file_path = os.path.join(category_path, file_name)
            
            title = f"{doc_num} {doc_name}"
            file_name_full = f"{doc_num}-{doc_name}.md"
            
            content = MAIN_MD_TEMPLATE.format(
                FILE_NAME=file_name_full,
                DESCRIPTION=doc_desc,
                VERSION=VERSION,
                CREATE_DATE=CREATE_DATE,
                STATUS=STATUS,
                TAGS=tags,
                TITLE=title,
                DOC_CATEGORY=category.replace("HaiLan-Pro-", ""),
                DOC_NAME=doc_name
            )
            
            with open(file_path, 'w', encoding=ENCODING) as f:
                f.write(content)
            
            print(f"✅ 已生成: {file_path}")
        
        readme_path = os.path.join(category_path, "README.md")
        readme_content = README_MD_TEMPLATE.format(
            VERSION=VERSION,
            CREATE_DATE=CREATE_DATE,
            STATUS=STATUS,
            TAGS="[文档索引]",
            DOC_CATEGORY=category.replace("HaiLan-Pro-", ""),
            DESCRIPTION=f"{category}文档索引"
        )
        
        with open(readme_path, 'w', encoding=ENCODING) as f:
            f.write(readme_content)
        
        print(f"✅ 已生成: {readme_path}")
        print()

    print("=== 文档生成完成 ===")
    print(f"共生成 {len(PROJECT_STRUCT)} 个目录")
    print(f"文档根目录: {DOC_ROOT}")

if __name__ == "__main__":
    main()
